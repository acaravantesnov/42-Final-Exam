#include <unistd.h>

int	main(void)
{
	int i;

	i = 122;
	while(i > 96)
	{
		if(i % 2 == 0)
			write(1, &i, 1);
		else
		{
			i -= 32;
			write(1, &i, 1);
			i += 32;
		}
		i--;
	}
	write(1, "\n", 1);
}